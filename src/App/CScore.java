    //Formats the image of the flag
    public ImageIcon scaleImage(ImageIcon icon, int w, int h) {
        int nw = icon.getIconWidth();
        int nh = icon.getIconHeight();

        if (nw > w) {
            nw = w;
            nh = (nw * icon.getIconHeight()) / icon.getIconWidth();
        }

        if (nh > h) {
            nh = h;
            nw = (icon.getIconWidth() * nh) / icon.getIconHeight();
        }

        return new ImageIcon(icon.getImage().getScaledInstance(nw, nh, Image.SCALE_DEFAULT));
    }

    //Gets the name and the flag of the country
    private Map<String, ImageIcon> createImageMap(Object[] list) {
        Map<String, ImageIcon> map = new HashMap<>();
        for (Object s : list) {
            ImageIcon icon = new ImageIcon(
                    new File(strFolder + "/" + s.toString() + ".png").getAbsolutePath());
            map.put(s.toString(), scaleImage(icon, 50, 50));
        }
        return map;
    }

